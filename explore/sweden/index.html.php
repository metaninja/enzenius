  <h3>
    About Sweden
  </h3>

  <div class="card">
    <section class="row">
      <div class="one-thirds">
        <h3 class="card-title">
          HISTORY
        </h3>
      </div>
      <div class="two-thirds">
        <p>Swedish history comprises more than 10,000 years and starts after the last glacial period when settlers arrived to the northern parts of the country from the east as well as from the south. Agriculture was introduced during the Stone Age. The process of creating political unity started about 1100 A.D. Over time, Sweden was influenced by other cultures such as the Germans during the middle Ages and the French in the 18th century. After winning wars against Denmark-Norway, Russia, and Poland during the 17th century, Sweden emerged as a Great Power. By the treaties of Brömsebro, 1645, and Roskilde, 1658, Sweden acquired important provinces of Denmark and Norway. Russia, Saxony-Poland, and Denmark-Norway pooled their power in 1700 and attacked the Swedish empire. In the subsequent peace treaties, the allied powers, joined by Prussia and by England-Hanover, ended Sweden's reign as a great power. From 1750 to 1850, agriculture went through a period of modernization, where it shifted gradually from village to private farm-based agriculture during the Industrial Revolution. This led to increased production but also a number of farm laborers without property. Some tried to find jobs in the towns; others left the country, mainly for America. During and after World War I, in which Sweden remained neutral, the country gained from the world-wide demand for Swedish steel, ball bearings, wood pulp, and matches. Post-war prosperity provided the foundations for the social welfare policies characteristic of modern Sweden. In 1994, a further step towards internationalism was taken, when the Swedish people decided in a referendum to join the EU.</p>
      </div>
    </section>
  </div>


  <div class="card">
