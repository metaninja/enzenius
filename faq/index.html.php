      <details>
        <summary>
          <h4>
            What is the possibility of studying medicine in  New Zealand?
          </h4>
        </summary>
        <p>
          Medicine is one of the most popular subjects for international students, and many
          who pursue such qualifications are choosing to study in New Zealand medical
          school due to the nation’s two highly regarded medical schools; The University of
          Auckland and the University of Otago. Both are known for their immersive study
          opportunities and fantastic research facilities, and IEC Abroad’s connections with
          them make it even easier to study medicine in New Zealand for international
          students.
        </p>
        <h5>
          Applying To Study Medicine In New Zealand
        </h5>
        <p>
          New Zealand medical school(s) have medical courses available at both Otago
          and Auckland Universities. It is the Bachelor of Medicine and Bachelor of Surgery
          qualification (MBChB). International applicants must have completed relevant
          courses in health sciences, biomedical sciences or similar before applying.
        </p>

        <p>
          The First Year BHSc or BSc in Biomedical Science is a prerequisite for domestic
          students. Your qualification should be of a similar type, as admission is often at
          the discretion of the university. Students who decide on studying in New
          Zealand medical school must also register for the Undergraduate Medicine and
          Health Sciences Admission Test (UMAT) test before June in their application
          year, with the actual test taking place in July.
        </p>

        <p>
          The score from this examination will be used to determine your suitability and
          challenges your critical thinking, communication skills, and scientific knowledge.
          Any prospective students whose first language is not English must also be able
          to prove their English language fluency by sitting the IELTS test. A minimum
          score of 7.5 is necessary, and students must not have a score lower than 7.0 in
          any manner. The IELTS is not required if you have already completed an
          undergraduate or postgraduate degree at a university in New Zealand previously.
        </p>
      </details>

